    //load all phones in the database (both available and disabled) on to the table
    private void loadAllPhones(){
        model.setRowCount(0);
        PhoneDAO dao=new PhoneDAO();
        try {
            list= dao.loadAllPhones(1);
            List<PhoneDTO> listOfDisable= dao.loadAllPhones(0);
            list.addAll(listOfDisable);
            for(PhoneDTO dto:list)
                model.addRow(dto.toVector());
        } catch (Exception ex) {
            Logger.getLogger(AdminFrame.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Error when loading data!");
        } 
    }
    
    //method to draw an image with the link 
    private void showImage(String imageLink) throws Exception{
            txtFailedImage.setText("");
            currentImage=ImageIO.read(new File(imageLink));
            if(currentImage!=null){
                //delete the old picture
                g.clearRect(x, y,pnlImage.getWidth()-x,pnlImage.getHeight()-y);
                //get size of the picture to draw
                int imgWidth = currentImage.getWidth();
                int imgHeight = currentImage.getHeight();
                double ratio = 1.0*imgWidth/imgHeight;
                int areaWidth = this.pnlImage.getWidth()-2*x;
                int areaHeight = this.pnlImage.getHeight()-2*y;
                //if the image is smaller than panel to draw --> make the panel smaller
                if(imgWidth<=areaWidth && imgHeight <=areaHeight){
                    areaHeight=imgHeight;
                    areaWidth=imgWidth;
                }else if(imgWidth>imgHeight){ 
                    if(imgWidth<areaWidth) areaWidth = imgWidth;
                    areaHeight = (int) (areaWidth/ratio);
                }else{
                    if(imgHeight<areaHeight)areaHeight = imgHeight;
                    areaWidth=(int) (areaHeight*ratio);
                }
                //after adjust the image and panel, draw picture
                g.drawImage(currentImage, x, y,areaWidth,areaHeight,this.pnlImage);
            }
    }
    
