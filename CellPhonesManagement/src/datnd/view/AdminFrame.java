    //check that no information is null
    private boolean validateInfor(String phoneID,String phonename,String maker,String description, String price, String image){
        boolean result=true;
        if(phoneID.length()==0|| phonename.length()==0||
           maker.length()==0|| description.length()==0||
                image.length()==0||price.length()==0)
        {
            result=false;
            JOptionPane.showMessageDialog(this, "Information must not be blank");
        }
        return result;
    }
    
    //insert a new phone into Phones table
    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        try{
            String phoneID= txtInsertPhoneID.getText();
            String phonename=txtInsertPhonename.getText();
            String maker=txtInsertMaker.getText();
            String description=txtInsertDescription.getText();
            String txtprice=txtInsertPrice.getText();
            String image=txtInsertImage.getText();
            if(!validateInfor(phoneID, phonename, maker, description, txtprice, image)){
                return;
            }
            int price=Integer.parseInt(txtprice);
            PhoneDTO dto=new PhoneDTO(phoneID, phonename, maker, description, image, price);
            PhoneDAO dao=new PhoneDAO();
            boolean result=dao.insertPhone(dto);
            if(result)
            {
                JOptionPane.showMessageDialog(this, "Successfull");
                model.addRow(dto.toVector());
                this.tabMenu.setSelectedIndex(0);
                this.tblView.setRowSelectionInterval(tblView.getRowCount()-1, tblView.getRowCount()-1);
            }
            else{
                JOptionPane.showMessageDialog(this, "Failed");
            }
        }
        catch(Exception e)
        {
            Logger.getLogger(AdminFrame.class.getName()).log(Level.SEVERE, null, e);
            if(e.getMessage().contains("duplicate")){
                JOptionPane.showMessageDialog(this, "PhoneID has existed");
                txtInsertPhoneID.setFocusable(true);
            }
            else{
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Failed");
            }
        }
    }//GEN-LAST:event_btnInsertActionPerformed

