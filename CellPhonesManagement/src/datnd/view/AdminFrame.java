    //when admin click on a row in the table, show the detail
    private void tblViewMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblViewMouseClicked
        row=tblView.getSelectedRow();
        if(row<-1)
            return;
        String phoneID=(String) tblView.getValueAt(row, 0);
        txtShowPhoneID.setText(phoneID);
        if(!list.get(row).isAvailable())
            rbShowIsDisabled.setSelected(true);
        else
            rbShowIsDisabled.setSelected(false);
        txtShowMaker.setText((String) tblView.getValueAt(row, 1));
        txtShowPhonename.setText((String) tblView.getValueAt(row, 2));
        txtShowDescription.setText((String) tblView.getValueAt(row, 3));
        txtShowPrice.setText(tblView.getValueAt(row, 4).toString());
        PhoneDAO dao=new PhoneDAO();
        try {
            linkImage = dao.getLinkImage(phoneID);
            if(linkImage!=null)
                showImage(linkImage);
            else 
                txtFailedImage.setText("Cannot show image!");
        } catch (Exception ex) {
            Logger.getLogger(AdminFrame.class.getName()).log(Level.SEVERE, null, ex);
            txtFailedImage.setText("Cannot show image!");
        }
    }//GEN-LAST:event_tblViewMouseClicked

