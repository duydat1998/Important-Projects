    private void btnUpdateCheckActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateCheckActionPerformed
        String phoneID=this.txtUpdatePhoneID.getText();
        PhoneDAO dao= new PhoneDAO();
        PhoneDTO dto;
        try {
            dto = dao.searchByPhoneID(phoneID);
            if(dto==null){
                JOptionPane.showMessageDialog(this, "The phone ID doesn't exist");
            }
            else{
                this.txtUpdatePhoneID.setEditable(false);
                this.txtUpdatePhonename.setText(dto.getPhonename());
                this.txtUpdateDescription.setText(dto.getDescription());
                this.txtUpdateMaker.setText(dto.getMaker());
                this.txtUpdateImage.setText(dto.getLink());
                this.txtUpdatePrice.setText(dto.getPrice()+"");
            }
        } catch (Exception ex) {
            Logger.getLogger(AdminFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnUpdateCheckActionPerformed

    //method to enable a phone
    private void btnEnablePhoneActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnablePhoneActionPerformed
        if(JOptionPane.showConfirmDialog(null, "Are you sure to enable this phone? ") != JOptionPane.YES_OPTION)
            return;
        String phoneID=txtDeletePhoneID.getText();
        if(phoneID.length()==0)
        {
            JOptionPane.showMessageDialog(null, "PhoneID is not null");
            return;
        }
        try {
            PhoneDAO dao= new PhoneDAO();
            boolean checkID=dao.isPhoneIDExisted(phoneID);
            if(checkID){
                boolean result=dao.enablePhone(phoneID);
                if(result)
                {
                    JOptionPane.showMessageDialog(this, "Successfull");
                    loadAllPhones();
                    this.tabMenu.setSelectedIndex(0);
                }
            }
            else{
                JOptionPane.showMessageDialog(this, "This PhoneID doesn't exist!");
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Failed");
        }
    }//GEN-LAST:event_btnEnablePhoneActionPerformed

