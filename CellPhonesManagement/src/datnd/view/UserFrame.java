    //update profile of the user
    private void btnUpdateProfileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateProfileActionPerformed
        String password=new String(txtUpdatePassword.getPassword());
        String fullname=txtUpdateFullname.getText();
        String role;
        if(rbAdmin.isSelected())
            role="admin";
        else
            role="user";
        UserDTO dto= new UserDTO(username, fullname, role, password);
        UserDAO dao=new UserDAO();
        try {
            boolean result=dao.updateUserProfile(dto);
            if(result)
            {
                JOptionPane.showMessageDialog(this, "Updated successfully!");
                if(role.equals("admin")){
                    JOptionPane.showMessageDialog(this, "Your role is changed, login again and continue your work!");
                    this.dispose();
                    new LoginJFrame().setVisible(true);
                }
            }
            else{
                JOptionPane.showMessageDialog(this, "Updated Failed!");
            }
        } catch (Exception ex) {
            Logger.getLogger(AdminFrame.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Failed");
        }

    }//GEN-LAST:event_btnUpdateProfileActionPerformed

