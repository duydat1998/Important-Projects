    //search phones based on inputed information
    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        try {
            String phonename=txtPhonename.getText();
            String maker=txtMaker.getText();
            String from=txtFromPrice.getText();
            String to=txtToPrice.getText();
            List<PhoneDTO> result=null;
            PhoneDAO dao=new PhoneDAO();
            if(phonename.length()!=0)
                result=dao.searchByPhonename(phonename);
            else if(maker.length()!=0)
                result=dao.searchByMaker(maker);
            else if(from.length()!=0&&to.length()==0)
            {
                //set 'to' to an infinite number
                to="1000000000";
                result=dao.searchByPrice(from, to);
            }
            else if(from.length()==0&&to.length()!=0)
            {
                //set 'from' to zero
                from="0";
                result=dao.searchByPrice(from, to);
            }
            else if(from.length()!=0&&to.length()!=0)
                result=dao.searchByPrice(from, to);
                
            searchModel.setRowCount(0);
            if(result.size()>0)
            {
                for(PhoneDTO dto:result)
                {
                    searchModel.addRow(dto.toVector());
                }
                lbCountResult.setText(result.size()+" Phone(s)");
            }
            else{
                lbCountResult.setText("No phone satisfy condition!");
            }
        } catch (Exception ex) {
            Logger.getLogger(UserFrame.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Failed");
        }
    }//GEN-LAST:event_btnSearchActionPerformed

